        public FakeUser()
        {
            FamilyName = _name[RandomNumber.Next(0, _name.Length)];
            Gender = RandomNumber.Next(1, 200) < 120;//安装男1.2,女1.0比例获取
            if (Gender)
            {
                FirstName = _boyName[RandomNumber.Next(0, _boyName.Length)];
            }
            else
            {
                FirstName = _girlName[RandomNumber.Next(0, _girlName.Length)];
            }

            bool isSingle = RandomNumber.Next(1, 1000) < 50;//5%的人名字是单名?
            if (isSingle) FirstName = FirstName.Substring(0, FirstName.Length - 1);

            // 中国现在的年龄中值是37岁,目前采用正态分布计算一个数字,
            // 可能需要安装中国的年龄分布情况来计算更好
            int rndAge = (int)Math.Round(Normal.Sample(RandomNumber._rnd, 37, 20));
            Age = Math.Max(0, rndAge);
            int year = DateTime.Now.Year - Age;
            int month = RandomNumber.Next(1, 13);
            //为了回避2月只有28号问题,我们从1号开始加随机小时数来确定出生日子
            int dayHours = RandomNumber.Next(0, 31 * 24);
            Birthday = new DateTime(year, month, 1).Add(TimeSpan.FromHours(dayHours));
            var _idNumPre = _IDAreaNum[RandomNumber.Next(0, _IDAreaNum.Length)]//区号
                            + Birthday.ToString("yyyyMMdd")//出生日期
                            + new Random().Next(0, 100).ToString("00")//随机号
                            + (Gender ? "1":"2");//男性为单数,女性为双数
            IdNum = _idNumPre + IdVerificationNum(_idNumPre);
        }

        /// <summary>
        /// 获取身份证校检码
        /// </summary>
        /// <param name="_idNumPre"></param>
        /// <returns></returns>
        internal static string IdVerificationNum(string _idNumPre)
        {
            var len = _idNumPre.Length;
            long sum = 0;
            int[] coefficients = { 7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2 };
            for(int i=0; i< len; i++)
            {
                var a = long.Parse(_idNumPre.Substring(i, 1));
                sum += a * coefficients[i];
            }
            string[] checks = { "1", "0", "X", "9", "8", "7", "6", "5", "4", "3", "2" };
            return checks[sum % 11];
        }

